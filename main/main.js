module.exports = function main(str) {

    const lcd = {
        "0": ["._.", "|.|", "|_|"],
        "1": ["...", "..|", "..|"],
        "2": ["._.", "._|", "|_."],
        "3": ["._.", "._|", "._|"],
        "4": ["...", "|_|", "..|"],
        "5": ["._.", "|_.", "._|"],
        "6": ["._.", "|_.", "|_|"],
        "7": ["._.", "..|", "..|"],
        "8": ["._.", "|_|", "|_|"],
        "9": ["._.", "|_|", "..|"]
      };

    return str
    .split("")
    .map(c => lcd[c])
    .reduce((x, y) => [x[0] + " " + y[0], x[1] + " " + y[1], x[2] + " " + y[2]])
    .map(l => l + "\n")
    .join("");

};

